const { parseMultipartData, sanitizeEntity } = require("strapi-utils");

module.exports = {
  /**
   * Create a record.
   *
   * @return {Object}
   */

  async create(ctx) {
    let cart = await strapi.services.cart.create(ctx.request.body);
    return sanitizeEntity(cart, { model: strapi.models.cart });
  }

};
